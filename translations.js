for (const code of Object.keys(LANGUAGES)) {
  if (!TRANSLATIONS[code]) {
    TRANSLATIONS[code] = {};
  }
}

let currentLang = localStorage.getItem(LANG_KEY) || FALLBACK;

function t(key) {
  const langData = TRANSLATIONS[currentLang];
  if (langData && langData[key] !== undefined) {
    return langData[key];
  }
  const fallbackData = TRANSLATIONS[FALLBACK];
  if (fallbackData && fallbackData[key] !== undefined) {
    return fallbackData[key];
  }
  return key;
}

function getMonths() {
  return MONTHS[currentLang] || MONTHS[FALLBACK];
}

function setLang(code) {
  if (!LANGUAGES[code]) return;
  currentLang = code;
  localStorage.setItem(LANG_KEY, code);
  applyTranslation();
}

function applyTranslation() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const translated = t(key);
    if (translated !== key || key.startsWith("_")) {
      el.textContent = translated;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(key);
  });

  const langSelect = document.getElementById("langSelect");
  if (langSelect) langSelect.value = currentLang;

  const langLabel = document.getElementById("langLabel");
  if (langLabel) langLabel.textContent = LANGUAGES[currentLang] || currentLang;

  document.documentElement.lang = currentLang;

  const event = new CustomEvent("langchange", { detail: { lang: currentLang } });
  document.dispatchEvent(event);
}

function buildLangDropdown(containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;

  container.innerHTML = `
    <div style="position:relative;display:inline-block">
      <select id="langSelect" style="appearance:none;-webkit-appearance:none;background:rgba(255,255,255,.06);color:#cbd5e1;border:1px solid rgba(255,255,255,.12);border-radius:8px;padding:8px 32px 8px 12px;font-size:12px;outline:none;cursor:pointer;font-family:inherit;min-width:140px;backdrop-filter:blur(4px)">
        <optgroup label="${t('more_lang')}" style="background:#1e293b;color:#cbd5e1">
          ${TOP_LANGS.map(c => `<option value="${c}" style="background:#0f172a;color:#cbd5e1">${LANGUAGES[c]}</option>`).join("")}
        </optgroup>
        <optgroup label="${t('more_lang')}" style="background:#1e293b;color:#cbd5e1">
          ${Object.keys(LANGUAGES).filter(c => !TOP_LANGS.includes(c)).map(c => `<option value="${c}" style="background:#0f172a;color:#cbd5e1">${LANGUAGES[c]}</option>`).join("")}
        </optgroup>
      </select>
      <span style="position:absolute;right:10px;top:50%;transform:translateY(-50%);pointer-events:none;color:#94a3b8;font-size:10px">&#9662;</span>
    </div>
  `;

  document.getElementById("langSelect").addEventListener("change", function() {
    setLang(this.value);
  });

  document.getElementById("langSelect").value = currentLang;
}

document.addEventListener("langchange", function(e) {
  document.querySelectorAll("#langSelect, #langSelect optgroup").forEach(function(el) {
    if (el.tagName === "OPTGROUP" && el.label) {
      el.label = t("more_lang");
    }
  });
});
